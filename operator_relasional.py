
# Operator ==

hasil = 20 == 25
print("20 == 25 = ", hasil)

# Operator !=

hasil = 20 != 25
print("20 != 25 = ", hasil)

# Operator >

hasil = 20 > 25
print("20 > 25 = ", hasil)

# Operator >=

hasil = 20 >= 20
print("20 >= 20 = ", hasil)

# Operator <

hasil = 20 < 21
print("20 < 21 = ", hasil)

# Operator <=

hasil = 22 <= 21
print("22 <= 21 = ", hasil)

# Operator in

teks = "belajar python"
print("Python" in teks)
